    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        Proveedor proveedores = new Proveedor();
        boolean guardado;

        if (txtNombre.getText().isEmpty() || txtApellido.getText().isEmpty() || txtCorreo.getText().isEmpty() || txtDireccion.getText().isEmpty() || txtTelefono.getText().isEmpty()) {
            JOptionPane.showInternalMessageDialog(this, "Porfavor no dejar campos vacios", "Notificacion del Sistema", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        if (Validar.isInt(txtTelefono.getText())) {
            proveedores.setTelefono(txtTelefono.getText());
        } else {
            JOptionPane.showInternalMessageDialog(this, "Cantidad invalida en inventario incial", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (cmbEstado.getSelectedItem().toString().equals("Activo")) {
            proveedores.setEstado(true);
        } else {
            proveedores.setEstado(false);
        }
        proveedores.setNombre(txtNombre.getText());
        proveedores.setApellido(txtApellido.getText());
        proveedores.setDireccion(txtDireccion.getText());
        proveedores.setCorreo(txtCorreo.getText());

        ProveedoresController p = new ProveedoresController();
        int id = p.RegistrarProveedor(proveedores);

        if (proveedorEnvio && id > 0) {
            JOptionPane.showInternalMessageDialog(this, "Guardado exitosamente,Procediendo a Envios", "Notificacion del Sistema", JOptionPane.INFORMATION_MESSAGE);
            Administracion_Envios ae = new Administracion_Envios(id);
            Centrar(ae, getDesktopPane());
            this.dispose();
        } else if (id > 0 && !proveedorEnvio) {
            this.txtNombre.setText(null);
            this.txtApellido.setText(null);
            this.txtDireccion.setText(null);
            this.txtCorreo.setText(null);
            this.txtTelefono.setText(null);
            limpiarTabla(dtm);
            JOptionPane.showInternalMessageDialog(this, "Guardado exitosamente", "Notificacion del Sistema", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showInternalMessageDialog(this, "Error al guardar", "Notificacion del Sistema", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

