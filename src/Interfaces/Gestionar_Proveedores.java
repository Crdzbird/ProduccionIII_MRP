    public void CargarProveedores() {
        ProveedoresController pc = new ProveedoresController();
        for (Proveedor p : pc.getAll()) {
            datos.addRow(new Object[]{p.getId(), p.getNombre(), p.getApellido(),
                p.getDireccion(), p.getCorreo(), p.getTelefono(), p.isEstado() ? "Activo" : "Inactivo"});
        }
    }

